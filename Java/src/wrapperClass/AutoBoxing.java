package wrapperClass;

public class AutoBoxing {

	public static void main(String[] args) {
		Byte a=19;
		Short c=12;
		Integer b=10;
		Long d=19089l;
		Float e=10.8f;
		Double s=123.0;
		Character v='s';
		Boolean m=true;
		System.out.println("Non-primitive Date types is :"+a);
		System.out.println("Non-primitive Date types is :"+b);
		System.out.println("Non-primitive Date types is :"+c);
		System.out.println("Non-primitive Date types is :"+d);
		System.out.println("Non-primitive Date types is :"+e);
		System.out.println("Non-primitive Date types is :"+s);
		System.out.println("Non-primitive Date types is :"+m);
		System.out.println("Non-primitive Date types is :"+v);
		
		

	}

}
